// i) Ler dois inteiros (variáveis A e B) e imprimir o resultado do quadrado da diferença do primeiro valor pelo segundo.

var A = parseInt(prompt('Digite o valor de A:'))
var B = parseInt(prompt('Digite o valor de B:'))
var quadrado_diferenca = (A - B)**2

alert('O quadrado da diferença dos números é ' + quadrado_diferenca + '.')