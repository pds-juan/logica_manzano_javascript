// l) Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à soma dos quadrados dos três valores lidos.

var A = parseFloat(prompt('Digite o valor de A:'))
var B = parseFloat(prompt('Digite o valor de B:'))
var C = parseFloat(prompt('Digite o valor de C:'))
var soma_quadrados = ((A**2)+(B**2)+(C**2))

alert('A soma dos quadrados dos três valores é '+soma_quadrados+'.')